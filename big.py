import turtle

display = turtle.Screen()
display.title("Turtle Graphics")
display.bgcolor("lightyellow")

head = turtle.Turtle()
head.shape("turtle")
head.color("blue")
head.forward(100)
head.left(90)
head.forward(400)
head.left(90)
head.left(90)

display.mainloop()
rgb = (255, 0, 0)  # Red color
head.color(rgb)

    

